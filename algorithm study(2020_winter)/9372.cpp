#include<iostream>
using namespace std;

int main(int argc, char* argv[]) {

    ios_base::sync_with_stdio(0);
    cin.tie(0);

    int T;
    cin >> T;
    while (T--) {
        int m, n;
        cin >> m >> n;
        for (int s = 0; s < n; s++) {
            int a, b;
            cin >> a >> b;
        }
        cout << m - 1 << endl;
    }


    return 0;
}
